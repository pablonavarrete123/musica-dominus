A diferencia de los viajeros turísticos en avión que conocen mucho más de las capitales de los países que visitan, estando cerca el aeropuerto y cómodos hoteles internacionales; personalmente, humilde viajero de caminos, sin horarios, sin constricciones, sin entender bien el objetivo de los viajes organizados, poco a poco comencé a sentirme invadido por este dulce salvajismo primitivo. Me sentí más cercano a los nativos que a la mayoría blanca de estos pequeños países centroamericanos. Con ellos aprendí a vivir de la forma más sencilla posible, en contacto con la naturaleza y tratando de saborear todo, especialmente el silencio. No me gusta hablar de pobreza; sino vivir de los productos frescos de la tierra, al ritmo de las estaciones, de sus buenas o malas cosechas. ¡La dureza de las condiciones y los caprichos del clima dan ganas de clamar justicia!</p>

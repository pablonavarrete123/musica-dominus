1. Equilibrio armónico.
Para principiantes, en su mayoría niños: requiere que operes ambas partes del cerebro.
La parte izquierda del cerebro corresponde a la lógica, procesará el aspecto funcional y matemático de la música, como la división de los sonidos en medias notas, negras, corcheas, semicorcheas, etc.
¡El lado derecho del cerebro se ocupará del arte, la melodía, el canto! ¡Porque no olvidemos que tocar el piano es simplemente hacer cantar a tu instrumento!
Consiguiendo coordinar las dos manos conseguiremos la armonía. Es casi una lucha entre la cabeza (mano izquierda) y el corazón (mano derecha) entre la expansión melódica y la tensión rítmica, entre la estructura y el ensueño. Lo más interesante</p>
